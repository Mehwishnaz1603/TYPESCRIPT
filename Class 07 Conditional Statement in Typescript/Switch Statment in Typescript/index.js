// Switch Statement:
// The switch statement evaluates an expression, matching the expression's value against a series of case clauses, 
// and executes statements after the first case clause with a matching value, until a break statement is encountered.
// Example:
//Choosing what to do in your day off based on the day. if it is Saturday. you go hickin, if its sunday you read a book 
//otherwise you work on a hobbey.
var a = "Sunday";
switch (a) {
    case "Saturady":
        console.log("Go hicking");
    case "Subday":
        console.log("Read a abook");
        break;
    case "default":
        console.log("Work on a Hobby");
}
