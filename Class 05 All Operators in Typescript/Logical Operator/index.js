// Logical operators:
// Logical operator are used to combine multiple conditions in one.
// Example:
var n = 5;
console.log(n >= 5 && n < 10); //
console.log(n > 5 && n < 10); //
console.log(n >= 5 || n < 10); //
console.log(n > 5 || n < 10); //
console.log(!(n < 10)); //
console.log(!(n > 10)); //
