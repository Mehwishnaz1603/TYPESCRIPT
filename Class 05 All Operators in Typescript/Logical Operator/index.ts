
// Logical operators:
// Logical operator are used to combine multiple Local conditions in one statement.
// Example:

// Example logicalAnd && logicalOR || and logicalNot !:
let n = 5;
console.log(n >= 5 && n < 10); // true  //LogicalAnd &&
console.log(n > 5 && n < 10); // false  //LogicalAnd &&
console.log(n >= 5 || n < 10); // true  //LogicalOR  ||
console.log(n > 5 || n < 10); // true   //LogicalOR  ||
console.log(!(n < 10)); // false        //LogicalISNOT !
console.log(!(n > 10)); // true         //LogicalISNOT !

